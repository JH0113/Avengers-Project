package model;

import java.util.Date;

public class OneOnOneDTO {
	String oneononeSub;
	String oneononeContents;
	String memName;
	String memId;
	Date oneononeDate;
	
	public String getOneononeSub() {
		return oneononeSub;
	}
	public void setOneononeSub(String oneononeSub) {
		this.oneononeSub = oneononeSub;
	}
	public String getOneononeContents() {
		return oneononeContents;
	}
	public void setOneononeContents(String oneononeContents) {
		this.oneononeContents = oneononeContents;
	}
	public String getMemName() {
		return memName;
	}
	public void setMemName(String memName) {
		this.memName = memName;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public Date getOneononeDate() {
		return oneononeDate;
	}
	public void setOneononeDate(Date oneononeDate) {
		this.oneononeDate = oneononeDate;
	}
	
}
