package command;

public class ProductCommand {
	String prodName;
	String prodPrice;
	String prodImage;
	String prodDetail;
	String prodLocation;
	String prodTag;
	String prodImageplus;
	
	public String getProdName() {
		return prodName;
	}
	public void setProdName(String prodName) {
		this.prodName = prodName;
	}
	public String getProdPrice() {
		return prodPrice;
	}
	public void setProdPrice(String prodPrice) {
		this.prodPrice = prodPrice;
	}
	public String getProdImage() {
		return prodImage;
	}
	public void setProdImage(String prodImage) {
		this.prodImage = prodImage;
	}
	public String getProdDetail() {
		return prodDetail;
	}
	public void setProdDetail(String prodDetail) {
		this.prodDetail = prodDetail;
	}
	public String getProdLocation() {
		return prodLocation;
	}
	public void setProdLocation(String prodLocation) {
		this.prodLocation = prodLocation;
	}
	public String getProdTag() {
		return prodTag;
	}
	public void setProdTag(String prodTag) {
		this.prodTag = prodTag;
	}
	public String getProdImageplus() {
		return prodImageplus;
	}
	public void setProdImageplus(String prodImageplus) {
		this.prodImageplus = prodImageplus;
	}
	
	
}
